/* distructuring */
function ActionItem({label, action}){
  return <li> {label} <a href="#">{action}</a></li> 
}

const allCountries = [
  "Russia",
  "USA",
  "China",
  "Spain",
  "Italy",
]

export default function Home() {

  const [selectedCountries , setSelectedCountries] = useState([]);

  function addHandle(label){
    setSelectedCountries( selectedCountries.concat(label) );
  }

  function removeHandle(label){
    const updateList = selectedCountries.filter( c => c !== label); 
    setSelectedCountries(updateList);
  }

  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <h1 className={styles.title}>
          Welcome to Tour Master
        </h1>

        <div className={styles.grid}>
          <div className={styles.card}>
            <h2>Selected</h2>
            <ul>
              {
                selectedCountries.map( country => <ActionItem key={country} label={country} action="-"/> )
              }
            </ul>
          </div>

          <div className={styles.card}>
            <h2>Choose From</h2>
            <ul>
              {
                allCountries.map( country => <ActionItem key={country} label={country} action="+"/> )
              }
            </ul>
          </div>

        </div>
      </main>

    </div>
  )
}
